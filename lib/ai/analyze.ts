export async function analyzeQuiz(input: AnalyzeInput): Promise<AiAnalysis> {
  const key = process.env.OPENAI_API_KEY;
  if (!key) {
    throw new Error(
      "Нет OPENAI_API_KEY. Добавьте ключ в файл portal/.env.local и перезапустите сервер."
    );
  }

  const outcome = OUTCOME[input.resultId];
  if (!outcome) {
    throw new Error("Неизвестный исход теста: " + input.resultId);
  }

  const system =
    SYSTEM_BASE +
    "\n\n=== ИСХОД ТЕСТА ===\n" +
    outcome.label +
    "\nРекомендованный путь/продукт: " +
    outcome.product +
    "\nОсобенность этого исхода: " +
    outcome.note +
    "\n\n=== БАЗА ЗНАНИЙ ===\n" +
    knowledgeFor(outcome.track);

  const situation = (input.situation || "").toString().trim().slice(0, 1500);
  const userMsg =
    "Ответы человека в тесте:\n" +
    JSON.stringify(input.answers || {}, null, 2) +
    (input.scores ? "\n\nБаллы по шкалам:\n" + JSON.stringify(input.scores) : "") +
    (situation
      ? "\n\nСитуация своими словами (обязательно учти в diagnosis и why):\n«" + situation + "»"
      : "\n\nСитуацию своими словами человек не описал — опирайся на ответы теста.") +
    "\n\nНапиши разбор строго в JSON.";

  const apiRes = await fetch("https://api.openai.com/v1/chat/completions", {
    method: "POST",
    headers: {
      Authorization: "Bearer " + key,
      "Content-Type": "application/json",
    },
    body: JSON.stringify({
      model: MODEL,
      temperature: 0.7,
      response_format: { type: "json_object" },
      messages: [
        { role: "system", content: system },
        { role: "user", content: userMsg },
      ],
    }),
  });

  const data = await apiRes.json();
  if (data.error) throw new Error(data.error.message);

  const parsed = JSON.parse(data.choices?.[0]?.message?.content || "{}");
  return {
    headline: String(parsed.headline || ""),
    diagnosis: String(parsed.diagnosis || ""),
    why: Array.isArray(parsed.why) ? parsed.why.map((x: unknown) => String(x)) : [],
    firstStep: String(parsed.firstStep || ""),
    honest: String(parsed.honest || ""),
  };
}

/** Ветка по 1-му вопросу (id q_intent). */
export function branchOf(ans: Record<string, string | string[]>): QuizBranch | null {
  const i = (ans["q_intent"] as string) || "";
  if (/продаю на Ozon/.test(i)) return "shop";
  if (/работаю менеджером/.test(i)) return "manager";
  return i ? "cold" : null;
}

/** Полный поток вопросов с учётом выбранной ветки. */
export function buildFlow(ans: Record<string, string | string[]>): QuizQuestion[] {
  const b = branchOf(ans);
  return b ? [...QUAL, ...BRANCH_Q[b]] : [...QUAL];
}
